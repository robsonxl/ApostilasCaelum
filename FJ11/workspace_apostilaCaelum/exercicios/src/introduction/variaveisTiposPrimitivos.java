package introduction;

public class variaveisTiposPrimitivos {
	
	
	public static  float balancoTrimestral(float valor1, float valor2, float valor3){
		float valorTrimestral = 0 ;
		valorTrimestral = valor1+valor2+valor3;
		return valorTrimestral;
	}
	
	public static float mediaMensalTrimetral(float valor1){
		float media = valor1/3;
		return media;
	}

}
